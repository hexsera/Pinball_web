def seed_notices(db, count: int = 40) -> int:
    """공지사항 mock 데이터 시딩. 삽입된 개수 반환."""
    existing_count = db.query(Notice).count()
    existing_titles = {n.title for n in db.query(Notice.title).all()}

    admin = db.query(User).filter(User.role == "admin").first()
    if not admin:
        print("Admin 계정이 없습니다. seed_admin()을 먼저 실행하세요.")
        return 0

    items = [n for n in NOTICES[:count] if n["title"] not in existing_titles]
    for data in items:
        notice = Notice(title=data["title"], content=data["content"], author_id=admin.id)
        db.add(notice)

    db.commit()
    print(f"공지사항 {len(items)}건 추가 완료. (기존 {existing_count}건)")
    return len(items)


if __name__ == "__main__":
    db = SessionLocal()
    try:
        seed_notices(db)
    finally:
        db.close()
